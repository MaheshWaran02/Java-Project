package oops;

public class EncapulationExample {
	
	private int Salary;
	private String Company;
	private int EnpID;
	
	
	public int getSalary() {
		return Salary;
	}
	public void setSalary(int salary) {
		Salary = salary;
	}
	public String getCompany() {
		return Company;
	}
	public void setCompany(String company) {
		Company = company;
	}
	public int getEnpID() {
		return EnpID;
	}
	public void setEnpID(int enpID) {
		EnpID = enpID;
	}

}
